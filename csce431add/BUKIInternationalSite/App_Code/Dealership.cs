using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace BUKI
{
    public class Dealership
    {
        private string m_name = string.Empty;
        private string m_location = string.Empty;
        private List<Salesperson> m_employees = new List<Salesperson>();
        private List<Vehicle> m_vehicles = new List<Vehicle>();
        private int m_id = 0;

        public string Name 
        {
            get { return m_name; }
            set { m_name = value; } 
        }
        public string Location 
        {
            get { return m_location; }
            set { m_location = value; } 
        }
        public List<Salesperson> Employees 
        {
            get { return m_employees; }
            set { m_employees = value; } 
        }
        public List<Vehicle> Vehicles 
        {
            get { return m_vehicles; }
            set { m_vehicles = value; } 
        }
        public int ID 
        {
            get { return m_id; }
            set { m_id = value; } 
        }
    }
}
